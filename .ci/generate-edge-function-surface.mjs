import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { resolve, basename } from 'node:path'

const root = process.cwd()

function sh(cmd) {
  return execSync(cmd, { cwd: root, encoding: 'utf8' }).trim()
}

function listFunctions() {
  const output = sh("find supabase/functions -mindepth 1 -maxdepth 1 -type d -not -name '_*' -exec basename {} \\\; | sort")
  return output.split('\n').map((line) => line.trim()).filter(Boolean)
}

function parseWrapperInvocations() {
  const output = sh("rg -n \"functions\\.invoke<|functions\\.invoke\\(\" packages/api/src -S")
  const rows = new Map()

  for (const line of output.split('\n')) {
    const [file] = line.split(':')
    const source = readFileSync(resolve(root, file), 'utf8')

    for (const match of source.matchAll(/functions\.invoke(?:<[\s\S]*?>)?\(\s*['"]([a-z0-9-]+)['"]/g)) {
      const fn = match[1]
      if (!rows.has(fn)) rows.set(fn, new Set())
      rows.get(fn).add(file)
    }
  }

  return rows
}

function parseInternalCallers() {
  const files = sh("rg --files supabase/functions -g 'index.ts'")
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)

  const callers = new Map()

  for (const file of files) {
    const fn = basename(resolve(file, '..'))
    const src = readFileSync(resolve(root, file), 'utf8')
    for (const match of src.matchAll(/functions\/v1\/([a-z0-9-]+)/g)) {
      const called = match[1]
      if (called === fn) continue
      if (!callers.has(called)) callers.set(called, new Set())
      callers.get(called).add(fn)
    }
  }

  return callers
}

const classificationOverrides = {
  public: new Set([
    'verify-serial',
    'get-fraud-warning',
  ]),
  authenticated: new Set([
    'check-availability',
    'commission-summary',
    'create-checkout-session',
    'determine-consultant-eligibility',
    'get-consultant-commission-earned',
    'get-consultant-pending-payouts',
    'get-consultant-units-sold',
    'get-referral-link',
    'get-unit-status',
    'list-products',
    'validate-order',
    'validate-serialized-units',
  ]),
  internal: new Set(['stripe-webhook', 'process-order-ledger']),
  scheduled: new Set([
    'aggregate-ledger-by-month',
    'calculate-gross',
    'calculate-royalties-owed',
    'compile-monthly-invoice',
    'generate-invoice-record',
    'generate-army-report',
    'generate-clc-report',
    'detect-duplicate-serials',
  ]),
}

function classify(fn) {
  if (classificationOverrides.public.has(fn)) return 'public'
  if (classificationOverrides.authenticated.has(fn)) return 'authenticated'
  if (classificationOverrides.internal.has(fn)) return 'internal'
  if (classificationOverrides.scheduled.has(fn)) return 'scheduled'
  return 'admin'
}

function owner(fn) {
  if (fn.includes('fraud')) return 'Fraud Ops'
  if (fn.includes('royalty') || fn.includes('invoice') || fn.includes('payout')) return 'Finance Ops'
  if (fn.includes('commission') || fn.includes('consultant')) return 'Consultant Ops'
  if (
    fn.includes('unit') || fn.includes('inventory') || fn.includes('serial') ||
    fn.includes('hologram') || fn.includes('lock') || fn.includes('availability')
  ) {
    return 'Inventory Ops'
  }
  if (fn.includes('product') || fn.includes('license-holder') || fn.includes('license')) return 'Catalog Ops'
  if (fn.includes('checkout') || fn.includes('order') || fn.includes('stripe')) return 'Commerce/Payments'
  return 'Platform'
}

function formatSet(set) {
  const values = [...set]
  return values.length === 0 ? 'none' : values.sort().join(', ')
}

const functions = listFunctions()
const wrapperInvocations = parseWrapperInvocations()
const internalCallers = parseInternalCallers()

const rows = functions.map((fn) => {
  const className = classify(fn)
  const wrappers = wrapperInvocations.get(fn) ?? new Set()
  const callers = new Set()

  for (const file of wrappers) callers.add(`api:${file.replace('packages/api/src/', '')}`)
  for (const caller of (internalCallers.get(fn) ?? new Set())) callers.add(`fn:${caller}`)
  if (className === 'scheduled') callers.add('scheduler:cron/manual job')

  const wrapperStatus = wrappers.size > 0 ? 'wrapped' : 'no-wrapper'

  return {
    function: fn,
    className,
    owner: owner(fn),
    callers: formatSet(callers),
    wrapperStatus,
  }
})

rows.sort((a, b) => a.function.localeCompare(b.function))

const inventoryLines = []
inventoryLines.push('# Edge Function Surface Inventory')
inventoryLines.push('')
inventoryLines.push('Generated by `.ci/generate-edge-function-surface.mjs`.')
inventoryLines.push('')
inventoryLines.push('| Function | Class | Owner | Primary Callers | Wrapper |')
inventoryLines.push('|---|---|---|---|---|')
for (const row of rows) {
  inventoryLines.push(`| ${row.function} | ${row.className} | ${row.owner} | ${row.callers} | ${row.wrapperStatus} |`)
}

const deadCandidates = rows.filter((row) =>
  row.className === 'admin' &&
  row.wrapperStatus === 'no-wrapper' &&
  row.callers === 'none',
)

const deadLines = []
deadLines.push('# Edge Function Dead Candidates')
deadLines.push('')
deadLines.push('Generated by `.ci/generate-edge-function-surface.mjs`.')
deadLines.push('')
if (deadCandidates.length === 0) {
  deadLines.push('No dead admin endpoints detected by static signal.')
} else {
  deadLines.push('Candidates are unwrapped, have no discovered internal caller, and are not classified as scheduled/public/authenticated/internal.')
  deadLines.push('')
  deadLines.push('| Function | Owner | Next Action |')
  deadLines.push('|---|---|---|')
  for (const row of deadCandidates) {
    deadLines.push(`| ${row.function} | ${row.owner} | Confirm usage; deprecate/remove if unused. |`)
  }
}

mkdirSync(resolve(root, 'docs'), { recursive: true })
writeFileSync(resolve(root, 'docs/edge-function-surface.md'), `${inventoryLines.join('\n')}\n`)
writeFileSync(resolve(root, 'docs/edge-function-dead-candidates.md'), `${deadLines.join('\n')}\n`)

console.log(`[GTG][surface] Wrote docs/edge-function-surface.md (${rows.length} functions).`)
console.log(`[GTG][surface] Wrote docs/edge-function-dead-candidates.md (${deadCandidates.length} candidates).`)
